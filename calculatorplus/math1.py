import math
def sq(a):
    return math.sqrt(a)

def ra(a):
    return math.radians(a)

def ce(a):
    return math.ceil(a)

def fl(a):
    return math.floor(a)

def de(a):
    return math.degrees(a)

def hy(a, b):
    return math.hypot(a, b)

def fa(a):
    return math.fabs(a)

def fm(a, b):
    return math.fmod(a, b)

def fac(a):
    return math.factorial(a)

def sin(a):
    return math.sin(math.radians(a))

def cos(a):
    return math.cos(math.radians(a))

def tan(a):
    return math.tan(math.radians(a))

